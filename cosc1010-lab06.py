# Output which letter occurred the most 

most_occurred = ""
least_occurred = ""

most=0
least=2500

for letter,count in sorted_letters.items():
    if count>most:
        most=count
        most_occurred=(f"'{letter}'")
    if count<least:
        least=count
        least_occurred=(f"'{letter}'")

